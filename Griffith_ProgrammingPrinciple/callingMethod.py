s="hello"

#normal built-in function 
print("length of", s, "=", len(s)) #pass s as argument to the function

#object is passed as extra argument to the method

#method of str class
print(s.isdigit()) #method is called on instance of str (s) using the method access operator, the period(.)

# isdigit() does not have extra arguments, but some methods do

